let message = 'He said, "Hello!"'; // using escape character for double quotes
console.log(message); // Output: He said, "Hello!"

let path = "C:\\Users\\John\\Documents"; // using escape character for backslash
console.log(path); // Output: C:\Users\John\Documents

let multiline = "First line\nSecond line"; // using escape character for newline
console.log(multiline);
// Output:
// First line
// Second line
